 package ruokuaidati;

public class DaAn {
	private String id;
	private String answer;
	
	public DaAn(String id, String answer) {
		super();
		this.id = id;
		this.answer = answer;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}

}
